// Import fs.
const fs = require('fs');

// Create the import file.
let importFile = fs.readdirSync(`${__dirname}/additionalIcons`).filter(x => x !== 'index.ts').map(x => `export {default as ${x.split('.').shift()}} from './${x.split('.').shift()}';`).join('\n');

// Add the header/end line.
importFile = `// AUTOGENERATED! DO NOT EDIT!\n${importFile}\n`;

// Write the file.
fs.writeFileSync(`${__dirname}/additionalIcons/index.ts`, importFile);
